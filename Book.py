class Book1:
    def __init__(self,Title,Author,Genre,Year):
        self.Title=Title 
        self.Author=Author 
        self.Genre=Genre 
        self.Year=Year 

    def getTitle(self):
        return self.Title 
    def getAuthor(self):
        return self.Author 
    def getGenre(self):
        return self.Genre
    def getYear(self):
        return self.Year     

